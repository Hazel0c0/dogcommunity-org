package company.friendsdog.dogcommunity.util;

public class MyObject {
  private String name;
  private int age;
  private String city;

  // Getter 메서드들

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getAge() {
    return age;
  }

  public void setAge(int age) {
    this.age = age;
  }

  public String getCity() {
    return city;
  }

  public void setCity(String city) {
    this.city = city;
  }
}